
public class Multiplosde3 {
	//multiplos de 3 entre 1 e 100 
	public static void main(String[] args) {
		//variavel numero=3;enquanto numero for menor que 100; adicionar o numero(3) a ele mesmo.
		for(int numero=3;numero<100;numero++) {
			//imprime a variavel numero enquanto seu valor for menor que 100
			System.out.println(numero);
			
		}
	}
}
